a = True
b = False

print('AND Logic needs both to be true') # If x is true and y is true then do this 
print('a and a =', a and a)
print('a and b =', a and b)
print('b and b =', b and b)

# the \n adds a line of space when printed out
print('\nOR Logic') # If either this or that is true then do something
print('a and a = ', a or a)
print('a or b = ', a or b)
print('b or b =', b or b)

print('\nNOT Logic')
print('a =', a, '\tnot a =', not a)
print('b =', b, '\tnot b =', not b)